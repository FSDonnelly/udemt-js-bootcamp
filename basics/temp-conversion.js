let farenheit = 50;

// calculate celsius and store in variable
// print the value

let celsius = (farenheit - 32) * (5/9);
console.log(`${farenheit} degrees Farenheit is equal to ${celsius} degrees Celsius.`);

// calculate kelvin and store in variable
// print the value

let kelvin = (farenheit + 459.67) * (5/9);
console.log(`${farenheit} degrees Farenheit is equal to ${kelvin} degrees Kelvin.`)